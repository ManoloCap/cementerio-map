/**
 * Return the subset of a slug's points that belong to `section`.
 * For sections without `members`, returns all points for the slug.
 * For sections with `members`, filters by the numeric tail of each point id (e.g. "7A-15" → 15).
 */
export function getSectionPoints(section, cemeteryPoints) {
  const allPoints = cemeteryPoints[section.slug] || []
  if (!section.members) return allPoints
  const { start, end } = section.members
  return allPoints.filter(p => {
    const n = parseInt(p.id.split('-')[1], 10)
    return n >= start && n <= end
  })
}
